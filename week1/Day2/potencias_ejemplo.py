# Ejemplos de potencias en Python
numero = 5

# Número al cuadrado (5²)
print(f"{numero} al cuadrado es: {numero**2}")  # resultado: 25

# Número al cubo (5³)
print(f"{numero} al cubo es: {numero**3}")      # resultado: 125

# Número a la cuarta (5⁴)
print(f"{numero} a la cuarta es: {numero**4}")  # resultado: 625

# Raíz cuadrada (√5)
print(f"La raíz cuadrada de {numero} es: {numero**0.5:.2f}")  # resultado: 2.24

# Raíz cúbica
print(f"La raíz cúbica de {numero} es: {numero**(1/3):.2f}")  # resultado: 1.71
